#include "libft.h"

int	ft_min(int *arr, int n)
{
	int i;
	int min;

	min = arr[0];
	i = 1;
	while (i < n)
	{
		if (arr[i] < min)
			min = arr[i];
		i++;
	}
	return (min);
}
